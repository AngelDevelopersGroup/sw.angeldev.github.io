let x = 0;

const friction = 0.8 / 60;
const el = document.querySelector('.pars');

function moveBackground() {
  x += (lFollowX - x) * friction;
  
  el.style['transform'] = `translateX(${x}vw)`;

  window.requestAnimationFrame(moveBackground);
}

